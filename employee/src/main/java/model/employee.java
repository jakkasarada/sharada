package model;

public class employee {
	private String empid;
	private String empname;
	private String empdesc;
	private String empphno;
	private String empadhar;
	public String getEmpid() {
		return empid;
	}
	public void setEmpid(String empid) {
		this.empid = empid;
	}
	public String getEmpname() {
		return empname;
	}
	public void setEmpname(String empname) {
		this.empname = empname;
	}
	public String getEmpdesc() {
		return empdesc;
	}
	public void setEmpdesc(String empdesc) {
		this.empdesc = empdesc;
	}
	public String getEmpphno() {
		return empphno;
	}
	public void setEmpphno(String empphno) {
		this.empphno = empphno;
	}
	public String getEmpadhar() {
		return empadhar;
	}
	public void setEmpadhar(String empadhar) {
		this.empadhar = empadhar;
	}
	

}
